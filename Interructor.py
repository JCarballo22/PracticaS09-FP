print("Seleccion de día")
dia = int(input("Digite un numero "))
match dia:
    case 1:
        print("lunes")
    case 2:
        print("Martes")
    case 3:
        print("Miercoles")
    case 4:
        print("Jueves")
    case 5:
        print("Viernes")
    case 6:
        print("Sábado")
    case 7:
        print("Domingo")

print("Fin")